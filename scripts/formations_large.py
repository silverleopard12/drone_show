# ============================================================================
# Helper function to get formations by drone count
# ============================================================================

def get_formations(num_drones):
    """Get start and target formations for given number of drones"""
    if num_drones == 10:
        return FORMATION_10_A, FORMATION_10_B
    elif num_drones == 25:
        return FORMATION_25_A, FORMATION_25_B
    elif num_drones == 36:
        return FORMATION_36_A, FORMATION_36_B
    else:
        raise ValueError(f"Unsupported number of drones: {num_drones}. Use 10, 25, or 36.")
